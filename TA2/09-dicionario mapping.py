# Criação de dicionario vazio, com atribuição posterior de chave e valor
dici_1 = {}
dici_1['nome'] = 'Gabriel'
dici_1['idade'] = 33


# Criação de dicionario usando elementos na forma, chave:valor

dici_2 = {'nome': 'Gabriel', 'idade': 33}

# Criação de dicionario com uma lista de tuplas, cada tupla representando um par de chave : valor

dici_3 = dict([('nome', 'Gabriel'), ('idade', 33)])

print(dici_3)